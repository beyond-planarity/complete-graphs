package data;

/**
 * Class to implement an integer-tuple.
 * @author tommy
 *
 */
public class Tuple {
	
	public int first;
	public int second;

	/**
	 * Creates a new <code>Tuple</code>.
	 * @param first		first entry in the tuple
	 * @param second	second entry in the tuple
	 */
	public Tuple(int first, int second) {
		this.first  = first;
		this.second = second;
	}
}
